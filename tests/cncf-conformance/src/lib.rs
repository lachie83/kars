/// C1 — every CRD declares at least one served + storage version.
pub fn check_crd_versions_served(crds: &[&Value]) -> Outcome {
    let mut missing = Vec::new();
    for c in crds {
        let kind = crd_kind(c).unwrap_or("?");
        let mut has_served = false;
        let mut has_storage = false;
        for v in crd_versions(c) {
            if v.get("served").and_then(Value::as_bool) == Some(true) {
                has_served = true;
            }
            if v.get("storage").and_then(Value::as_bool) == Some(true) {
                has_storage = true;
            }
        }
        if !(has_served && has_storage) {
            missing.push(kind.to_string());
        }
    }
    if missing.is_empty() {
        Outcome::Pass
    } else {
        Outcome::Fail(format!("CRDs missing served/storage version: {missing:?}"))
    }
}

