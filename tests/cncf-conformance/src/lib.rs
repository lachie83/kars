/// C7 — every Deployment has both liveness + readiness probes.
pub fn check_deployment_probes(manifests: &BTreeMap<PathBuf, Vec<Value>>) -> Outcome {
    let mut missing: Vec<String> = Vec::new();
    for docs in manifests.values() {
        for d in docs {
            if d.get("kind").and_then(Value::as_str) != Some("Deployment") {
                continue;
            }
            let name = d
                .get("metadata")
                .and_then(|m| m.get("name"))
                .and_then(Value::as_str)
                .unwrap_or("?")
                .to_string();
            let containers = d
                .get("spec")
                .and_then(|s| s.get("template"))
                .and_then(|t| t.get("spec"))
                .and_then(|s| s.get("containers"))
                .and_then(Value::as_sequence);
            let Some(containers) = containers else {
                missing.push(format!("{name} (no containers)"));
                continue;
            };
            for c in containers {
                let cname = c
                    .get("name")
                    .and_then(Value::as_str)
                    .unwrap_or("?")
                    .to_string();
                let has_liveness = c.get("livenessProbe").is_some();
                let has_readiness = c.get("readinessProbe").is_some();
                if !(has_liveness && has_readiness) {
                    missing.push(format!("{name}/{cname}"));
                }
            }
        }
    }
    if missing.is_empty() {
        Outcome::Pass
    } else {
        Outcome::Fail(format!(
            "Deployments missing liveness/readiness: {missing:?}"
        ))
    }
}

/// C8 — operator namespace has a default-deny NetworkPolicy applied.
pub fn check_default_deny_netpol(manifests: &BTreeMap<PathBuf, Vec<Value>>) -> Outcome {
    for docs in manifests.values() {
        for d in docs {
            if d.get("kind").and_then(Value::as_str) != Some("NetworkPolicy") {
                continue;
            }
            let ns = d
                .get("metadata")
                .and_then(|m| m.get("namespace"))
                .and_then(Value::as_str)
                .unwrap_or("");
            // Accept either the literal `azureclaw-system` namespace
            // or any policy whose name starts with `default-deny`
            // (Helm renders `{{ .Release.Namespace }}` to the
            // operator install namespace).
            let name = d
                .get("metadata")
                .and_then(|m| m.get("name"))
                .and_then(Value::as_str)
                .unwrap_or("");
            let is_system = ns == "azureclaw-system" || ns.is_empty();
            if !is_system {
                continue;
            }
            let policy_types = d
                .get("spec")
                .and_then(|s| s.get("policyTypes"))
                .and_then(Value::as_sequence);
            let pod_selector_empty = d
                .get("spec")
                .and_then(|s| s.get("podSelector"))
                .map(|ps| {
                    ps.as_mapping()
                        .map(|m| {
                            m.is_empty()
                                || m.get(Value::String("matchLabels".into()))
                                    .and_then(Value::as_mapping)
                                    .map(|x| x.is_empty())
                                    .unwrap_or(false)
                        })
                        .unwrap_or(false)
                })
                .unwrap_or(false);
            let covers_egress_ingress = policy_types
                .map(|s| {
                    let names: Vec<&str> = s.iter().filter_map(Value::as_str).collect();
                    names.contains(&"Egress") && names.contains(&"Ingress")
                })
                .unwrap_or(false);
            if pod_selector_empty && covers_egress_ingress {
                return Outcome::Pass;
            }
            // Tolerate the legacy explicit-name convention as a hint.
            let _ = name;
        }
    }
    Outcome::Fail("no default-deny NetworkPolicy found in azureclaw-system".into())
}

/// C9 — every image reference declares an explicit tag or digest.
/// Untagged refs (which implicitly resolve to `:latest`) are
/// forbidden because they make rollbacks ambiguous. The AzureClaw
/// repo convention pins `:latest` as the *explicit* default tag for
/// controller / inference-router / sandbox images (see
/// `docs/operations/supply-chain.md`); production deployments are
/// expected to override the tag at install time with a digest pin
/// or a chart-`appVersion`-derived tag. This check enforces the
/// minimum bar — no implicit-:latest images — without conflicting
/// with the repo convention.
pub fn check_image_pinning(manifests: &BTreeMap<PathBuf, Vec<Value>>) -> Outcome {
    let mut bad = Vec::new();
    for (path, docs) in manifests {
        for d in docs {
            if d.get("kind").and_then(Value::as_str) != Some("Deployment") {
                continue;
            }
            let containers = d
                .get("spec")
                .and_then(|s| s.get("template"))
                .and_then(|t| t.get("spec"))
                .and_then(|s| s.get("containers"))
                .and_then(Value::as_sequence);
            let Some(containers) = containers else {
                continue;
            };
            for c in containers {
                let Some(image) = c.get("image").and_then(Value::as_str) else {
                    continue;
                };
                // Image must contain either `:` (tag) or `@` (digest).
                // Strip the registry host (everything before the first `/`)
                // so a hostname like `localhost:5000/foo` doesn't false-pass.
                let after_host = image.rsplit_once('/').map(|(_, r)| r).unwrap_or(image);
                let has_tag_or_digest = after_host.contains(':') || after_host.contains('@');
                if !has_tag_or_digest {
                    bad.push(format!("{}: {image}", path.display()));
                }
            }
        }
    }
    if bad.is_empty() {
        Outcome::Pass
    } else {
        Outcome::Fail(format!(
            "untagged image refs in production manifests: {bad:?}"
        ))
    }
}

