/// C12 — at least one printer column per CRD references either
/// `.status.conditions` or `.status.phase` (the canonical "what's
/// the operator-observed state?" surface).
pub fn check_state_printer_column(crds: &[&Value]) -> Outcome {
    let mut missing = Vec::new();
    for c in crds {
        let kind = crd_kind(c).unwrap_or("?");
        let mut found = false;
        for v in crd_versions(c) {
            let Some(cols) = v
                .get("additionalPrinterColumns")
                .and_then(Value::as_sequence)
            else {
                continue;
            };
            for col in cols {
                let Some(jp) = col.get("jsonPath").and_then(Value::as_str) else {
                    continue;
                };
                if jp.contains(".status.conditions") || jp == ".status.phase" {
                    found = true;
                    break;
                }
            }
            if found {
                break;
            }
        }
        if !found {
            missing.push(kind.to_string());
        }
    }
    if missing.is_empty() {
        Outcome::Pass
    } else {
        Outcome::Fail(format!(
            "CRDs without status-state printer column: {missing:?}"
        ))
    }
}

/// C13 — every Deployment runs as non-root with a seccomp profile.
pub fn check_pod_security_baseline(manifests: &BTreeMap<PathBuf, Vec<Value>>) -> Outcome {
    let mut bad = Vec::new();
    for docs in manifests.values() {
        for d in docs {
            if d.get("kind").and_then(Value::as_str) != Some("Deployment") {
                continue;
            }
            let name = d
                .get("metadata")
                .and_then(|m| m.get("name"))
                .and_then(Value::as_str)
                .unwrap_or("?")
                .to_string();
            let pod_sec = d
                .get("spec")
                .and_then(|s| s.get("template"))
                .and_then(|t| t.get("spec"))
                .and_then(|s| s.get("securityContext"));
            let runs_as_nonroot = pod_sec
                .and_then(|s| s.get("runAsNonRoot"))
                .and_then(Value::as_bool)
                == Some(true);
            let has_seccomp = pod_sec.and_then(|s| s.get("seccompProfile")).is_some();
            if !(runs_as_nonroot && has_seccomp) {
                bad.push(name);
            }
        }
    }
    if bad.is_empty() {
        Outcome::Pass
    } else {
        Outcome::Fail(format!(
            "Deployments missing runAsNonRoot or seccompProfile: {bad:?}"
        ))
    }
}

/// C14 — supply-chain CI rows are wired (cargo-deny, cosign-verify,
/// Trivy, npm audit). We assert by reading `.github/workflows/ci.yml`
/// and checking for the row markers — this catches accidental
/// row-removals in PR review.
pub fn check_supply_chain_rows() -> Outcome {
    let path = repo_root().join(".github/workflows/ci.yml");
    let Ok(text) = std::fs::read_to_string(&path) else {
        return Outcome::Fail(format!("cannot read {}", path.display()));
    };
    let required = [
        ("cargo-deny", "cargo deny check"),
        ("trivy", "aquasecurity/trivy-action"),
        ("npm-audit", "npm audit --audit-level=high"),
        ("cosign-verify", "cosign-verify"),
    ];
    let mut missing = Vec::new();
    for (label, marker) in required {
        if !text.contains(marker) {
            missing.push(label);
        }
    }
    if missing.is_empty() {
        Outcome::Pass
    } else {
        Outcome::Fail(format!("ci.yml missing rows: {missing:?}"))
    }
}

/// C15 — `deny.toml` is present at workspace root and configures
/// the four required cargo-deny sections.
pub fn check_deny_config() -> Outcome {
    let path = repo_root().join("deny.toml");
    let Ok(text) = std::fs::read_to_string(&path) else {
        return Outcome::Fail("deny.toml missing".into());
    };
    let required_sections = ["[advisories]", "[licenses]", "[bans]", "[sources]"];
    let mut missing: Vec<&str> = required_sections
        .iter()
        .filter(|s| !text.contains(*s))
        .copied()
        .collect();
    if missing.is_empty() {
        Outcome::Pass
    } else {
        missing.sort();
        Outcome::Fail(format!("deny.toml missing sections: {missing:?}"))
    }
}

/// Run every conformance criterion and return a populated report.
pub fn run_all_checks() -> ConformanceReport {
    let manifests = load_repo_manifests();
    let crds = collect_crds(&manifests);
    let mut report = ConformanceReport::default();
    let mut push = |id, criterion, outcome| {
        report.results.push(CheckResult {
            id,
            criterion,
            outcome,
        });
    };
    push(
        "C1",
        "Every CRD has a served + storage version",
        check_crd_versions_served(&crds),
    );
    push(
        "C2",
        "Every CRD has additional printer columns",
        check_printer_columns(&crds),
    );
    push(
        "C3",
        "Every CRD status declares a conditions[] array",
        check_conditions_array(&crds),
    );
    push(
        "C4",
        "Every CRD spec is a structural schema",
        check_structural_schema(&crds),
    );
    push(
        "C5",
        "Every CRD ships at least one CEL x-kubernetes-validations rule",
        check_cel_validations(&crds),
    );
    push(
        "C6",
        "Every CRD declares a status subresource",
        check_status_subresource(&crds),
    );
    push(
        "C7",
        "Every Deployment has liveness + readiness probes",
        check_deployment_probes(&manifests),
    );
    push(
        "C8",
        "Operator namespace has a default-deny NetworkPolicy",
        check_default_deny_netpol(&manifests),
    );
    push(
        "C9",
        "Every image ref declares an explicit tag or digest (no implicit :latest)",
        check_image_pinning(&manifests),
    );
    push(
        "C10",
        "Every CRD carries app.kubernetes.io/name label",
        check_recommended_labels(&crds),
    );
    push(
        "C11",
        "Every CRD declares a valid scope",
        check_scope(&crds),
    );
    push(
        "C12",
        "Every CRD exposes a status-state printer column",
        check_state_printer_column(&crds),
    );
    push(
        "C13",
        "Every Deployment runs non-root with a seccompProfile",
        check_pod_security_baseline(&manifests),
    );
    push(
        "C14",
        "ci.yml wires the supply-chain rows (cargo-deny, trivy, npm audit, cosign-verify)",
        check_supply_chain_rows(),
    );
    push(
        "C15",
        "deny.toml configures advisories/licenses/bans/sources",
        check_deny_config(),
    );
    report
}
