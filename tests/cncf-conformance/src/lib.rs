//! K8s AI Conformance v1.35+ test suite for AzureClaw.
//!
//! See `tests/cncf-conformance/README.md` for the criterion list. The
//! suite is intentionally **manifest-driven**: it walks the rendered
//! Helm templates under `deploy/helm/azureclaw/templates/` and asserts
//! shape invariants without requiring a running K8s cluster. This
//! keeps PR CI fast and side-effect-free; the same suite can also be
//! invoked against `kustomize`-rendered or `helm template`-rendered
//! output in a kind cluster (Phase 3 deliverable).
//!
//! Public entry points:
//!
//! - [`load_repo_manifests`] — discover all `*.yaml` under the helm
//!   templates dir and parse each into a list of `serde_yaml::Value`
//!   documents (multi-doc YAML aware).
//! - [`run_all_checks`] — apply every conformance criterion to the
//!   loaded manifest set and return a [`ConformanceReport`].

#![deny(clippy::all)]

use std::collections::BTreeMap;
use std::path::PathBuf;

use serde::Deserialize;
use serde_yaml::Value;

/// Outcome of a single conformance criterion.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum Outcome {
    Pass,
    Fail(String),
}

impl Outcome {
    pub fn is_pass(&self) -> bool {
        matches!(self, Outcome::Pass)
    }
}

/// One row in the report — a CNCF criterion paired with its result.
#[derive(Debug, Clone)]
pub struct CheckResult {
    pub id: &'static str,
    pub criterion: &'static str,
    pub outcome: Outcome,
}

/// Aggregate report. `pass` / `fail` counts derive from `results`.
#[derive(Debug, Clone, Default)]
pub struct ConformanceReport {
    pub results: Vec<CheckResult>,
}

impl ConformanceReport {
    pub fn pass_count(&self) -> usize {
        self.results.iter().filter(|r| r.outcome.is_pass()).count()
    }

    pub fn fail_count(&self) -> usize {
        self.results.len() - self.pass_count()
    }

    pub fn all_passed(&self) -> bool {
        self.fail_count() == 0
    }

    /// Render the markdown report consumed by
    /// `tests/cncf-conformance/CONFORMANCE-REPORT.md`. Stable order
    /// (insertion order); stable formatting so a regenerated report
    /// produces a reproducible diff.
    pub fn to_markdown(&self) -> String {
        let mut out = String::new();
        out.push_str("# AzureClaw — CNCF K8s AI Conformance Report\n\n");
        out.push_str(
            "Generated by `cargo run -p azureclaw-cncf-conformance --bin cncf-conformance`.\n\n",
        );
        out.push_str(&format!(
            "**Status:** {} / {} criteria pass.\n\n",
            self.pass_count(),
            self.results.len()
        ));
        out.push_str("| ID | Criterion | Result |\n");
        out.push_str("|----|-----------|--------|\n");
        for r in &self.results {
            let (mark, detail) = match &r.outcome {
                Outcome::Pass => ("✅", String::new()),
                Outcome::Fail(msg) => ("❌", format!(" — {msg}")),
            };
            out.push_str(&format!(
                "| {} | {} | {}{} |\n",
                r.id, r.criterion, mark, detail
            ));
        }
        out
    }
}

/// Resolve the repository root.
pub fn repo_root() -> PathBuf {
    // CARGO_MANIFEST_DIR points at tests/cncf-conformance/.
    let manifest = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    manifest
        .parent()
        .and_then(|p| p.parent())
        .map(|p| p.to_path_buf())
        .unwrap_or(manifest)
}

/// All Helm template manifests, grouped by source file. The Helm
/// chart is rendered with `helm template` so multi-doc, conditional,
/// and value-templated content all parse as plain YAML. If `helm`
/// is not on PATH the loader falls back to parsing the static
/// `crd*.yaml` set under `deploy/helm/azureclaw/templates/` (CRDs
/// have no Helm tokens) — this keeps the conformance checks that
/// only need CRD shape running on minimal envs.
pub fn load_repo_manifests() -> BTreeMap<PathBuf, Vec<Value>> {
    let root = repo_root();
    let mut out = BTreeMap::new();

    if let Some(text) = render_helm(&root) {
        // `helm template` output is one big multi-doc YAML stream
        // with `# Source: ...` comments separating each rendered
        // file. Group by source so the per-path BTreeMap stays
        // meaningful (some checks key on path).
        for (source, body) in split_by_source(&text) {
            let key = root.join(source);
            let docs: Vec<Value> = serde_yaml::Deserializer::from_str(&body)
                .filter_map(|d| Value::deserialize(d).ok())
                .filter(|v| !v.is_null())
                .collect();
            if !docs.is_empty() {
                out.entry(key).or_insert_with(Vec::new).extend(docs);
            }
        }
        return out;
    }

    // Fallback path — only the static CRD YAMLs.
    let dir = root.join("deploy/helm/azureclaw/templates");
    if !dir.exists() {
        return out;
    }
    for entry in walkdir::WalkDir::new(&dir).into_iter().flatten() {
        let path = entry.path();
        if !path.is_file() {
            continue;
        }
        let name = path.file_name().and_then(|s| s.to_str()).unwrap_or("");
        if !(name.starts_with("crd") && name.ends_with(".yaml")) {
            continue;
        }
        let Ok(text) = std::fs::read_to_string(path) else {
            continue;
        };
        let docs: Vec<Value> = serde_yaml::Deserializer::from_str(&text)
            .filter_map(|d| Value::deserialize(d).ok())
            .filter(|v| !v.is_null())
            .collect();
        if !docs.is_empty() {
            out.insert(path.to_path_buf(), docs);
        }
    }
    out
}

fn render_helm(root: &std::path::Path) -> Option<String> {
    let chart = root.join("deploy/helm/azureclaw");
    if !chart.exists() {
        return None;
    }
    let output = std::process::Command::new("helm")
        .arg("template")
        .arg("ac")
        .arg(&chart)
        .arg("--namespace")
        .arg("azureclaw-system")
        .output()
        .ok()?;
    if !output.status.success() {
        return None;
    }
    String::from_utf8(output.stdout).ok()
}

fn split_by_source(rendered: &str) -> Vec<(String, String)> {
    let mut out: Vec<(String, String)> = Vec::new();
    let mut current_source = String::from("unknown");
    let mut buf = String::new();
    for line in rendered.lines() {
        if line.starts_with("---") {
            if !buf.trim().is_empty() {
                out.push((current_source.clone(), std::mem::take(&mut buf)));
            }
            continue;
        }
        if let Some(s) = line.strip_prefix("# Source: ") {
            current_source = s.trim().to_string();
            continue;
        }
        buf.push_str(line);
        buf.push('\n');
    }
    if !buf.trim().is_empty() {
        out.push((current_source, buf));
    }
    out
}

/// All CRD documents across the loaded manifests.
pub fn collect_crds(manifests: &BTreeMap<PathBuf, Vec<Value>>) -> Vec<&Value> {
    let mut crds = Vec::new();
    for docs in manifests.values() {
        for d in docs {
            if d.get("kind").and_then(Value::as_str) == Some("CustomResourceDefinition") {
                crds.push(d);
            }
        }
    }
    crds
}

fn crd_kind(crd: &Value) -> Option<&str> {
    crd.get("spec")?.get("names")?.get("kind")?.as_str()
}

fn crd_versions(crd: &Value) -> Vec<&Value> {
    crd.get("spec")
        .and_then(|s| s.get("versions"))
        .and_then(Value::as_sequence)
        .map(|v| v.iter().collect())
        .unwrap_or_default()
}

fn version_schema(version: &Value) -> Option<&Value> {
    version.get("schema")?.get("openAPIV3Schema")
}

